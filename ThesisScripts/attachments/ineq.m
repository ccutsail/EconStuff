function [out] = ineq(x,tb,te,ti,paramvec)

% This is the steady-state system without a government budget constraint

% Assign parameters
% alp, D1,D2,etae,etah,A, bp,   bt,  n1,  n2
alp = paramvec(1); D1 = paramvec(2); D2 = paramvec(3); 
etae = paramvec(4); etah = paramvec(5); A = paramvec(6);
bp = paramvec(7); bt = paramvec(9); n1 = paramvec(10); 
n2 = paramvec(8);

% Assign vector of state variables
k = x(1); h = x(2); 
c01 = x(3); c21 = x(4); s1 = x(5); e1 = x(6); h1 = x(7);
c02 = x(8); c22 = x(9); s2 = x(10); e2 = x(11); h2 = x(12);
w = x(13); R = x(14);

% Use FOCs to get output
out = [1-((1-ti)*w*D1^(1-(etae-1)/etae))/(1+te)*h1^((1-etah)*(etae-1)/etae+etah);...
       1-((1-ti)*w*D2^(1-(etae-1)/etae))/(1+te)*h2^((1-etah)*(etae-1)/etae+etah);...
       s1 - ((alp*A*bt*bp*(1-tb))^(1/(1-alp))*h-s2*n2)/n1;...
       s2 - ((alp*A*bt*bp*(1-tb))^(1/(1-alp))*h-s1*n1)/n2;...
       c21 - bt*(((1-ti)*w*h1+(1-tb)*R*s1-s1-c01-(1+te)*e1))/(1+bt*(1-tb));...
       c01 - bp*((1-ti)*w*h1+(1-tb)*(R*s1-c21)-s1-(1+te)*e1)/(1+bp);...
       c22 - bt*(((1-ti)*w*h2+(1-tb)*R*s2-s2-c02-(1+te)*e2))/(1+bt*(1-tb));...
       c02 - bp*((1-ti)*w*h2+(1-tb)*(R*s2-c22)-s2-(1+te)*e2)/(1+bp);...
       k - (s1*n1+s2*n2);...
       h - (h1*n1+h2*n2);...
       e1 - (h1^(1-etah)/D1)^(1/etae);...
       e2 - (h2^(1-etah)/D2)^(1/etae);...
       w - (1-alp)*A*k^alp*h^(-alp);...
       R - alp*A*k^(alp-1)*h^(1-alp)];
    
   
